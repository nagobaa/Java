/*
Använd en Boolean variabel 
för att representera om lampan är på eller av.
Använd if-else 
för att skriva ut om lampan är på eller av.
*/

public class NewJavaFile {
        
    public static void main(String args []) {
        
        //boolean-värden
        boolean TheLampIsOn = true;
        
        //fråga till användaren
        System.out.print("Is the lamp on?");
        
        //if, else
        if (TheLampIsOn = true){
            System.out.println(" " + TheLampIsOn);
        } else {
            System.out.println("false");
        }
    }
}

